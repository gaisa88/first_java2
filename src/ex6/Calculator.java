package ex6;

public class Calculator {
    int umnozhenie(int x, int y) {
        int z = x * y;
        return z;
    }

    long umnozhenie(long x, long y) {
        long z = x * y;
        return z;
    }

    double umnozhenie(double x, double y) {
        double z = x * y;
        return z;
    }
}

